
import java.util.*;

class CircleArea{

	public static void main(String args[]){

		Scanner sc = new Scanner(System.in);
		System.out.println("Enter the Radius of Circle");
		int rad = sc.nextInt();

		double Area = 3.14 * (rad * rad);
		System.out.println("Area of Circle is : " + Area);
		
	
	}

}