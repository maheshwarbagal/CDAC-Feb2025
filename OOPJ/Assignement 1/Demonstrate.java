class Demonstrate{

	public static void main(String [] args){
		int binary = 0b0110;//binary number
		int octal = 012 ;//octal should have start with 0
		int hexa = 0x34Bc;
		float f = 23.04f;
		double d = 34.54;
		double scientific = 2.3e4;

		System.out.println("Binary : "+ binary);
		System.out.println("Octal : "+octal);
		System.out.println("hexa : "+hexa);
		System.out.println("Floating num : "+f);
		System.out.println("Double : "+d);
		System.out.println("Scientific Num : "+scientific);      
		

	}	

}