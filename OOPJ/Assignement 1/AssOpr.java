class AssOpr{

	public static void main(String []args){
		int a = 5;// " = " is assignment Operator
		int b = 6;

		a += b; // "+=" value of b is added in a;
		System.out.println(a);

		a -= b; //updated value of b is subtracted from a;
		System.out.println(a);
		a *= b; //" *= " value of b is multiplied with updated value of A
		System.out.println(a);
		a /= b; //"/=" b is divided from a
		System.out.println(a);
		a %= b; //"%=" b modulus a is calculated

		
	
	}

}