class Swap{

	public static void main(String args[]){

		int FirstNumber = 10;
		int SecondNumber = 20;

		System.out.println("Before Swapping");
		System.out.println(FirstNumber);
		System.out.println(SecondNumber);

		System.out.println();

		System.out.println("After Swapping");
		System.out.println(SecondNumber);
		System.out.println(FirstNumber);

	}

}